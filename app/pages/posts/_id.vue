<template>
  <div>
    <button @click="$fetch">
      Refresh
    </button>
    <template v-if="$fetchState.pending">
      <content-placeholders>
        <content-placeholders-heading/>
        <content-placeholders-text :lines="10"/>
      </content-placeholders>
    </template>
    <template v-else-if="$fetchState.error">
      <h1>Post #{{ $route.params.id }} not found</h1>
    </template>
    <template v-else>
      <h1>{{ post.title }}</h1>
      <author :user-id="post.userId"/>
      <pre>{{ post.body }}</pre>
      <p>
        <n-link :to="{ name: 'posts-id', params: { id: post.id + 1 } }">
          Next article
        </n-link>
      </p>
    </template>
    <p>
      <n-link to="/">
        Home
      </n-link>
    </p>
  </div>
</template>

<script lang="ts">
import {defineComponent, ref, useContext, useFetch,} from '@nuxtjs/composition-api'

import Author from '~/app/components/Author.vue'
import {$axios} from "~/app/utils/api";

export default defineComponent({
  components: {
    Author,
  },
  setup() {
    const post = ref()

    const {params} = useContext()

    useFetch(async () => {
      post.value = await $axios.$get(
          `https://jsonplaceholder.typicode.com/posts/${params.value.id}`
      )
    })

    return {post}
  },
})
</script>
